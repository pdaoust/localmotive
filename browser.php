			<li><em>Windows Vista users</em> can upgrade from version 7 to version 9 by running Windows Update or by <a href="http://www.microsoft.com/windows/downloads/ie/getitnow.mspx">installing from Microsoft's website</a>.</li>
			<li><em>Windows 7 users</em> are lucky enough to already have IE8 installed! For the best experience, however, we recommend <a href="http://www.microsoft.com/windows/downloads/ie/getitnow.mspx">upgrading to version 9</a>.</li>
		</ul>
	</li>
	<li>
		<h4>Mozilla Firefox</h4>

<h2></h2>
<p class="intro">If you've arrived at this page, you must have seen a message asking you to upgrade your browser.</p>
<p>The World Wide Web changes quickly, and we do our best to support all web standards and recent web browsers. Unfortunately, some ancient web browsers &mdash; we're not pointing any fingers, but this means Internet Explorer 6 and 7 &mdash; just can't keep up with the new standards. The site may still work for you, but certain features may not, and it may not look as good as our developer, Paul, designed it to look.</p>
<p>That's why we recommend that everyone upgrade to the newest version of their web browser. Our personal favourites are Mozilla Firefox and Google Chrome, but Internet Explorer is decent too &mdash; as long as you are using version 8 or newer.</p>
<h3>how to upgrade</h3>
<ul class="box">
	<li>
		<h4>Internet Explorer</h4>
